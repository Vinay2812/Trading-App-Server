SELECT
  Doc_No,
  Doc_Date,
  Ac_Code,
  Ac_Id,
  Ac_Name_E,
  System_Name_E,
  brandname,
  Inword_No,
  Item_Code,
  Brand_Code,
  No_Of_Bags,
  Wt_Per,
  Net_Wt,
  Company_Code,
  Year_Code,
  ISNULL(SUM(Bags), 0) AS Dispatched,
  No_Of_Bags - ISNULL(SUM(Bags), 0) AS Balance,
  csdetailid,
  Inword_Date
FROM
  dbo.qryColdStorageBalanceDetail
GROUP BY
  Doc_No,
  Doc_Date,
  Ac_Code,
  Ac_Id,
  Ac_Name_E,
  System_Name_E,
  brandname,
  Inword_No,
  Item_Code,
  Brand_Code,
  No_Of_Bags,
  Wt_Per,
  Net_Wt,
  Company_Code,
  Year_Code,
  csdetailid,
  Inword_Date;